    public void CreateServicio() {
        int Codigo = Integer.parseInt(jTextCodigo.getText());
        String Nombre = jTextNombre.getText();
        String Area = jComboBox1.getSelectedItem().toString();
        Servicios S = new Servicios(Codigo, Nombre, Area);
        ListaCircularDoble LCD=new ListaCircularDoble();
        try {
            FileInputStream miArchivo2;
            miArchivo2 = new FileInputStream("Servicios.su");
            ObjectInputStream input = new ObjectInputStream(miArchivo2);
            ListaCircularDoble vLCD = (ListaCircularDoble) input.readObject();
            input.close();
            miArchivo2.close();
            LCD = vLCD;
        } catch (Exception ex) {
            System.out.println("Exception: " + ex.getMessage());

        }
        LCD.insertarFinal(S); 
        try {

            FileOutputStream miArchivo = new FileOutputStream("Servicios.su");
            ObjectOutputStream output = new ObjectOutputStream(miArchivo);
            output.writeObject(LCD);
            output.close();
            miArchivo.close();
            JOptionPane.showMessageDialog(null, "Se registr√≥ correctamente en el Servicio");
            LCD.listarHaciaAdelante();
            this.dispose();

        } catch (Exception ex) {
            System.out.println("Exception: " + ex.getMessage());
        }
    }
